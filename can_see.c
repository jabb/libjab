#include <stdlib.h>
#include "can_see.h"

void path_to(int x0, int y0, int x1, int y1, int *path, int sz)
{
    int dx = abs(x1 - x0), sx = x0 < x1 ? 1 : -1;
    int dy = abs(y1 - y0), sy = y0 < y1 ? 1 : -1;
    int err = (dx > dy ? dx : -dy) / 2, e2;
    int i = 0;

    for (;;) {
        if (i >= sz - 1) break;
        path[i * 2 + 0] = x0;
        path[i * 2 + 1] = y0;
        i++;

        if (x0 == x1 && y0 == y1) break;
        e2 = err;
        if (e2 > -dx) { err -= dy; x0 += sx; }
        if (e2 <  dy) { err += dx; y0 += sy; }
    }

    path[i * 2 + 0] = -1;
    path[i * 2 + 1] = -1;
}

int can_see(int x0, int y0, int x1, int y1, int vis, int *sight, int width)
{
    int dx = abs(x1 - x0), sx = x0 < x1 ? 1 : -1;
    int dy = abs(y1 - y0), sy = y0 < y1 ? 1 : -1;
    int err = (dx > dy ? dx : -dy) / 2, e2;
    int prevx = x0, prevy = y0;
    double dist = 0.0;

    while (x0 != x1 || y0 != y1) {
        if (sight[y0 * width + x0] || dist >= vis - 1) return 0;

        dist += ((x0 - prevx) & (y0 - prevy)) ? 1.414 : 1.0;
        prevx = x0;
        prevy = y0;

        e2 = err;
        if (e2 > -dx) { err -= dy; x0 += sx; }
        if (e2 <  dy) { err += dx; y0 += sy; }
    }
    return 1;
}

